package com.misterpops.towerknight.Entities.Tile;

public class Coord {
	
	private float x;
	private float y;

	public Coord(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public Coord getCoord() {
		return this;
	}
	
	public float getX() {
		return this.x;
	}
	
	public float getY() {
		return this.y;
	}
	
}
